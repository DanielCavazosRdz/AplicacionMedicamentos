package Models;

/**
 *
 * @author Daniel
 */
public class Usuario {
    private int id;
    private String nombre;
    private String pass;
    private int rol;
    
    Usuario(){
    }
    
    Usuario(String nombre, String pass, int rol){
        this.nombre = nombre;
        this.pass = pass;
        this.rol = rol;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public int getRol() {
        return rol;
    }

    public void setRol(int rol) {
        this.rol = rol;
    }

}
